<p>This tab lists each link in the message with a summary of the number of clicks.
</p>
<table>
<tr>
<td>Heading</td>
<td>This shows the message ID and subject, and the list or lists to which the message has been sent.</td>
</tr>
<tr>
<td>Download</td>
<td>The fields displayed can be downloaded as a CSV file.</td>
</tr>
<tr>
<td>Link URL</td>
<td>The URL in the message; if the URL is longer than 39 characters then a shortened version is displayed. 
The URL is a link to the Link Clicks tab that shows the users who have clicked the link.</td>
</tr>
<tr>
<td>Personalised</td>
<td>Whether phplist will personalise the URL when the link is clicked. For URLs that contain a uid parameter phplist stores the URL
without that parameter so that only one URL is tracked instead of a separate URL for each subscriber.</td>
</tr>
<tr>
<td>Clicks</td>
<td>This is the total number of times that the link has been clicked.
A user clicking the same link twice is counted twice.
</td>
</tr>
<tr>
<td>Users</td>
<td>The number of users who have clicked the link in the message.</td>
</tr>
<tr>
<td>First</td>
<td>The earliest time that any user clicked the link. The value is empty if the link has not been clicked.</td>
</tr>
<tr>
<td>Latest</td>
<td>The most recent time that any user clicked the link. This value is empty if the link has not been clicked or has been clicked only once.</td>
</tr>
</table>
